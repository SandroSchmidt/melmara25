

/**
 * Normalisiert eine beliebige Datenstruktur für Mapbox:
 * - findet alle 2er-Arrays aus Zahlen (vermutete Koordinatenpaare)
 * - erkennt (lat, lng) und wandelt zu (lng, lat)
 * - begrenzt lat auf [-90, 90]
 * - bringt lng in den Bereich [-180, 180]
 * Gibt eine tiefe Kopie zurück; Original bleibt unverändert.
 */
function normalizeForMapbox(input) {
  const isNum = (v) => typeof v === 'number' && Number.isFinite(v);
  const toNum = (v) => Number.isFinite(v) ? v : Number(v);

  const clamp = (x, min, max) => Math.min(max, Math.max(min, x));
  const wrap180 = (lng) => {
    // bringt jeden Längengrad in [-180, 180]
    let x = lng % 360;
    if (x > 180) x -= 360;
    if (x <= -180) x += 360;
    return x;
  };

  const isCoordPair = (arr) =>
    Array.isArray(arr) &&
    arr.length === 2 &&
    isNum(toNum(arr[0])) &&
    isNum(toNum(arr[1]));

  function fixPair(pair) {
    let a = toNum(pair[0]); // könnte lat oder lng sein
    let b = toNum(pair[1]); // könnte lat oder lng sein

    // Heuristik zur Erkennung der Reihenfolge:
    // Mapbox erwartet [lng, lat] mit |lng|<=180, |lat|<=90
    // Häufige Quelle ist [lat, lng]: dann gilt |a|<=90 und |b|<=180, oft |b|>90.
    let swap = false;

    const absA = Math.abs(a);
    const absB = Math.abs(b);

    if (absA <= 90 && absB > 90 && absB <= 180) {
      // klarer Fall: [lat, lng] -> tauschen
      swap = true;
    } else if (absA > 90 && absB <= 90) {
      // klarer Fall: [lng, lat] -> nicht tauschen
      swap = false;
    } else if (absA <= 90 && absB <= 90) {
      // beide <= 90: unklar, belassen (könnte z.B. im Bereich liegen)
      swap = false;
    } else if (absA > 180 || absB > 180) {
      // Werte außerhalb üblicher Grade — wir nehmen an, dass b lng ist und tauschen, wenn das hilft
      // Wenn nach Tausch b (lat) <= 90 wird und a (lng) in [-180, 180] gebracht werden kann, tauschen
      const maybeLat = a, maybeLng = b;
      if (Math.abs(maybeLat) <= 90) swap = true;
    }

    let lng = swap ? b : a;
    let lat = swap ? a : b;

    // Normieren
    lng = wrap180(lng);
    lat = clamp(lat, -90, 90);

    return [lng, lat];
  }

  function walk(node) {
    if (isCoordPair(node)) {
      return fixPair(node);
    }
    if (Array.isArray(node)) {
      return node.map(walk);
    }
    if (node && typeof node === 'object') {
      const out = {};
      for (const k of Object.keys(node)) {
        out[k] = walk(node[k]);
      }
      return out;
    }
    return node; // primitive Werte unverändert
  }

  return walk(input);
}




